// Функція для отримання випадкових питань для теми
function getRandomQuestionsForTheme(count = 16) {
    // Створюємо копію масиву, щоб не мутувати оригінал
    const shuffled = [...allFrazeologizmyQuestions];
    // Алгоритм Фішера-Єйтса для якісного перемішування
    for (let i = shuffled.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    // Повертаємо перші 'count' питань
    return shuffled.slice(0, count);
}

